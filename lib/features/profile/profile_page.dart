class ProfilePage extends StatefulWidget {
  final VoidCallback onLogout;
  const ProfilePage({super.key, required this.onLogout});

  @override
  State<ProfilePage> createState() => _ProfilePageState();
}

class _ProfilePageState extends State<ProfilePage> {
  int _tab = 0;

  @override
  Widget build(BuildContext context) {
    final user = MockRepo.user;

    return Scaffold(
      appBar: AppBar(
        title: const Text('Profile'),
        actions: [
          IconButton(
            icon: const Icon(Icons.settings_outlined),
            onPressed: () => _system(
              'Profile settings',
              'Manage notifications, privacy, payout rules and custody preferences from one place.',
              type: PawneoNoticeType.info,
            ),
          ),
        ],
      ),
      body: ListView(
        padding: const EdgeInsets.fromLTRB(20, 12, 20, 120),
        children: [
          _ProfileHero(user: user),
          const SizedBox(height: 18),
          _ProfileTabs(
            selected: _tab,
            onChanged: (i) => setState(() => _tab = i),
          ),
          const SizedBox(height: 18),
          AnimatedSwitcher(
            duration: const Duration(milliseconds: 260),
            child: KeyedSubtree(
              key: ValueKey(_tab),
              child: switch (_tab) {
                0 => _OverviewTab(user: user),
                1 => _TrustTab(user: user, onAction: _system),
                _ => _RewardsTab(onAction: _system),
              },
            ),
          ),
          const SizedBox(height: 24),
          _SettingsGroup(
            onTap: (label) => _system(
              label,
              _copyFor(label),
              type: label == 'Security & privacy' ? PawneoNoticeType.warning : PawneoNoticeType.info,
            ),
            onLogout: widget.onLogout,
          ),
        ],
      ),
    );
  }

  String _copyFor(String label) {
    switch (label) {
      case 'Payout settings':
        return 'Choose payout currency, bank account and automatic reinvestment preferences for yield generated by your collateral.';
      case 'Security & privacy':
        return 'Biometric access, device approvals and data controls keep Pawneo secure before high-value objects are pledged.';
      case 'Notifications':
        return 'Get alerts for valuation changes, custody events, pool openings and payout cycles.';
      case 'Legal & terms':
        return 'Review custody, collateral, insurance and execution terms before activating a pawning agreement.';
      default:
        return 'The Pawneo support team can help with valuations, shipping, insurance and portfolio matching.';
    }
  }

  void _system(
    String title,
    String message, {
    PawneoNoticeType type = PawneoNoticeType.info,
  }) {
    PawneoSystemSheet.show(
      context,
      type: type,
      title: title,
      message: message,
      primaryLabel: 'Got it',
    );
  }
}

